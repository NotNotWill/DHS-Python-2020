####################
# Celcius          #
####################

print("CELSIUS CONVERSION")

fahrenheit =input("What is the temperature in Fahrenheit? ")

celsius = (int(fahrenheit) - 32)* 5/9

print(celsius)


####################
#      Kelvin      #
####################

print("KELVIN CONVERSION")

fahrenheit =input("What is the temperature in Fahrenheit? ")

kelvin = (int(fahrenheit) - 32)* 5/9 + 273.15

print(kelvin)

